#include <iostream>
using namespace std ;

int main() {
  // decrementing for loop 

  for(int i = 99 ; i > 0 ; i--)
  {
     cout << i << " bottles of beer on the wall" << endl ; 
     cout << i << " bottles of beer" << endl ;
     cout << "Take one down, pass it around " << endl ;
     cout << "<------------------------------------------->" << endl ;
  } 
 
  return 0 ; 
}